/**
 * 千分位格式化
 *
 * 题目描述：
 * 将数字（或数字字符串）格式化为带千分位分隔符的字符串，如 1234567.89 → "1,234,567.89"。
 *
 * 约定：
 * - 支持整数和小数；小数部分不插逗号
 * - 支持字符串数字输入，先按数字解析再格式化
 *
 * 思路：
 * 1. 拆成整数部分和小数部分
 * 2. 整数部分从右往左每三位插一个逗号（可用正则或循环）
 *
 * 时间复杂度：O(n)，n 为数字位数
 * 空间复杂度：O(n)
 *
 * @param {number | string} num 待格式化的数字
 * @returns {string}
 */

function formatThousands(num) {
  const n = Number(num);
  if (Number.isNaN(n)) {
    throw new TypeError("num 必须是有效数字或数字字符串");
  }
  const [intPart, decPart] = String(n).split(".");
  // 正则说明（零基础版）：
  // - \B：匹配“非词边界”，即两个字符之间的空隙（不能是字符串最开头或最结尾的“边界”）。用来在数字之间找插入逗号的位置。
  // - (?=...)：正向前瞻，表示“右边必须是...”，但匹配到的内容不包含在结果里，只用来定位”。
  // - (\d{3})+：\d 表示一个数字，{3} 表示重复 3 次，(\d{3}) 即“连续三个数字”；+ 表示这一段可以重复多次，即 3、6、9… 个数字。
  // - (?!\d)：负向前瞻，表示“右边不能再是数字”。这样保证我们只在“整数部分的末尾”插逗号，不会在最后不足三位的地方插。
  // 合起来：在整数部分里，找到“右边是若干组连续三位数字、且再右边没有数字”的空隙，在该空隙处替换成逗号，即从右往左每三位加一个逗号。
  const formattedInt = intPart.replace(/\B(?=(\d{3})+(?!\d))/g, ",");
  return decPart != null ? `${formattedInt}.${decPart}` : formattedInt;
}

// ---------------------------------------------------------------------------
// 示例
// ---------------------------------------------------------------------------

console.log("=== 千分位格式化测试 ===");

console.log(formatThousands(1234567.89));
console.log(formatThousands(1234567890));
console.log(formatThousands("1234567.89"));

module.exports = formatThousands;
