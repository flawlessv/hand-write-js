/**
 * LeetCode 287. 寻找重复数
 * https://leetcode.cn/problems/find-the-duplicate-number/
 *
 * 题目描述：
 * 给定一个包含 n + 1 个整数的数组 nums，其数字都在 [1, n] 范围内（包括 1 和 n），
 * 可知至少存在一个重复的整数。
 *
 * 假设 nums 只有一个重复的整数，返回这个重复的数。
 *
 * 要求：
 * - 不能修改原数组
 * - 只能使用 O(1) 的额外空间
 *
 * 示例 1：
 * 输入：nums = [1, 3, 4, 2, 2]
 * 输出：2
 *
 * 示例 2：
 * 输入：nums = [3, 1, 3, 4, 2]
 * 输出：3
 *
 * 示例 3：
 * 输入：nums = [3, 3, 3, 3, 3]
 * 输出：3
 *
 * 提示：
 * - 1 <= n <= 10^5
 * - nums.length == n + 1
 * - 1 <= nums[i] <= n
 * - nums 中只有一个整数出现两次或多次，其余整数均只出现一次
 *
 * 解法思路：Floyd 判圈算法（快慢指针）
 *
 * 这题看起来是数组题，但可以转换成“链表找环入口”问题。
 *
 * 一、为什么可以转成链表？
 * - 把“下标”看作节点，把“nums[i]”看作从 i 指向 nums[i] 的 next 指针
 * - 由于 nums[i] 的取值范围一定在 [1, n]，所以每个位置都会指向一个合法下标
 * - 从下标 0 开始不断跳转：0 -> nums[0] -> nums[nums[0]] -> ...
 *
 * 例如 nums = [1, 3, 4, 2, 2]
 * - 0 -> 1 -> 3 -> 2 -> 4 -> 2 -> 4 -> 2 ...
 * - 可以看到节点 2 被重复进入，因此形成了环
 *
 * 二、为什么一定有环？
 * - 数组长度是 n + 1，但数值只会落在 [1, n] 这 n 个位置里
 * - 相当于有 n + 1 个节点，不断指向 n 个可能的位置
 * - 根据抽屉原理，至少有两个位置会指向同一个数字，也就会在“跳转路径”里形成环
 *
 * 三、为什么“环的入口”就是重复数字？
 * - 某个数字重复出现，意味着有多个下标都会指向这个数字
 * - 在“链表视角”里，这个数字对应的位置就是被多条路径汇入的点
 * - 这个汇合点正是环的入口，也就是我们要求的重复数字
 *
 * 四、怎么找环入口？
 * 1. 用快慢指针找第一次相遇点
 *    - slow 每次走 1 步：slow = nums[slow]
 *    - fast 每次走 2 步：fast = nums[nums[fast]]
 *    - 如果有环，它们一定会在环内某处相遇
 *
 * 2. 找到环入口
 *    - 让 slow 回到起点 0
 *    - 然后 slow 和 fast 都每次走 1 步
 *    - 两者再次相遇的位置，就是环入口，也就是重复数字
 *
 * 五、为什么重置后再次相遇就是答案？
 * 设：
 * - 从起点 0 到环入口的距离为 a
 * - 从环入口到第一次相遇点的距离为 b
 * - 环长度为 c
 *
 * 第一次相遇时：
 * - slow 走了 a + b
 * - fast 走了 a + b + k * c（k >= 1，表示 fast 在环里多转了 k 圈）
 *
 * 因为 fast 的速度是 slow 的 2 倍，所以：
 * - 2(a + b) = a + b + k * c
 * - a + b = k * c
 * - a = k * c - b
 *
 * 这说明：
 * - 从起点走 a 步能到环入口
 * - 从第一次相遇点再走 a 步，也等价于在环里走 k * c - b 步，最终也会落在环入口
 *
 * 因此，把一个指针放回起点，另一个留在相遇点，同时每次走 1 步，
 * 它们会在环入口相遇，这个入口值就是重复数字。
 *
 * 时间复杂度：O(n)
 * 空间复杂度：O(1)
 */
function findDuplicate(nums: number[]): number {
    // slow、fast 都从下标 0 出发。
    // 这里的“走一步”不是 +1，而是跳到 nums[i] 指向的下一个位置。
    let slow = 0
    let fast = 0

    // 第一阶段：找到环中的相遇点。
    while (true) {
        // 慢指针一次走一步。
        slow = nums[slow]

        // 快指针一次走两步。
        fast = nums[nums[fast]]

        // 在环内相遇，说明已经确认存在环。
        if (slow === fast) break
    }

    // 第二阶段：slow 回到起点 0。
    // 此时 fast 留在第一次相遇点。
    slow = 0

    // 两个指针都改为一次走一步。
    // 再次相遇的位置就是环入口，也就是重复数字。
    while (slow !== fast) {
        slow = nums[slow]
        fast = nums[fast]
    }

    return slow
}
