/**
 * LeetCode 121. 买卖股票的最佳时机
 * https://leetcode.cn/problems/best-time-to-buy-and-sell-stock/
 * 
 * 题目描述：
 * 给定一个数组 prices ，它的第 i 个元素 prices[i] 表示一支给定股票第 i 天的价格。
 * 你只能选择 某一天 买入这只股票，并选择在 未来的某一个不同的日子 卖出该股票。
 * 设计一个算法来计算你所能获取的最大利润。
 * 返回你可以从这笔交易中获取的最大利润。如果你不能获取任何利润，返回 0 。
 * 
 * 示例 1：
 * 输入：[7,1,5,3,6,4]
 * 输出：5
 * 解释：在第 2 天（股票价格 = 1）的时候买入，在第 5 天（股票价格 = 6）的时候卖出，最大利润 = 6-1 = 5 。
 *      注意利润不能是 7-1 = 6, 因为卖出价格需要大于买入价格；同时，你不能在买入前卖出股票。
 * 
 * 示例 2：
 * 输入：prices = [7,6,4,3,1]
 * 输出：0
 * 解释：在这种情况下, 没有交易完成, 所以最大利润为 0。
 * 
 * 解题思路（贪心算法）：
 * 1. 只需要遍历一次价格数组
 * 2. 维护两个变量：min（到目前为止的最低价格）和 max（最大利润）
 * 3. 对于每一天的价格：
 *    - 更新最低价格 min = Math.min(当前价格, min)
 *    - 计算如果今天卖出的利润 = 当前价格 - min
 *    - 更新最大利润 max = Math.max(今天卖出的利润, max)
 * 4. 贪心策略：在遍历过程中，总是记录到目前为止的最低买入价格，并计算当前卖出能获得的最大利润
 * 
 * 时间复杂度：O(n)
 * 空间复杂度：O(1)
 */
function maxProfit(prices: number[]): number {
    // 边界条件：数组为空则返回0
    if(prices.length === 0) return 0
    
    // min：记录到目前为止遇到的最低价格（最佳买入点）
    let min = prices[0]
    // max：记录能够获得的最大利润
    let max = 0
    
    // 从第二天开始遍历
    for(let i = 1; i < prices.length; i++){
        // 更新最低买入价格
        min = Math.min(prices[i], min)
        // 计算今天卖出的利润，并更新最大利润
        // 贪心：在每个位置都尝试以当前价格卖出，看能否获得更大利润
        max = Math.max((prices[i] - min), max)
    }
    
    return max
};
export { maxProfit };