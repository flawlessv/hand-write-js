/**
 * LeetCode 122. 买卖股票的最佳时机 II
 * https://leetcode.cn/problems/best-time-to-buy-and-sell-stock-ii/
 * 
 * 题目描述：
 * 给定一个数组 prices ，其中 prices[i] 表示股票第 i 天的价格。
 * 在每一天，你可能会决定购买和/或出售股票。你在任何时候 最多 只能持有 一股 股票。
 * 你也可以购买它，然后在同一天出售。
 * 返回你能获得的最大利润。
 * 
 * 示例 1：
 * 输入：prices = [7,1,5,3,6,4]
 * 输出：7
 * 解释：在第 2 天（股票价格 = 1）的时候买入，在第 3 天（股票价格 = 5）的时候卖出, 这笔交易所能获得利润 = 5-1 = 4 。
 *      随后，在第 4 天（股票价格 = 3）的时候买入，在第 5 天（股票价格 = 6）的时候卖出, 这笔交易所能获得利润 = 6-3 = 3 。
 *      总利润 = 4 + 3 = 7 。
 * 
 * 示例 2：
 * 输入：prices = [1,2,3,4,5]
 * 输出：4
 * 解释：在第 1 天（股票价格 = 1）的时候买入，在第 5 天 （股票价格 = 5）的时候卖出, 这笔交易所能获得利润 = 5-1 = 4 。
 *      注意你不能在第 1 天和第 2 天接连购买股票，之后再将它们卖出。
 *      因为这样属于同时参与了多笔交易，你必须在再次购买前出售掉之前的股票。
 * 
 * 示例 3：
 * 输入：prices = [7,6,4,3,1]
 * 输出：0
 * 解释：在这种情况下, 没有交易完成, 所以最大利润为 0。
 * 
 * 解题思路（贪心算法）：
 * 1. 这道题可以进行多次交易，关键是找到所有的上升区间
 * 2. 贪心策略：只要第二天的价格比第一天高，就在第一天买入，第二天卖出
 * 3. 这样可以捕获所有的上升利润
 * 4. 例如：[1,2,3,4,5] 可以理解为：
 *    - 第1天买入第2天卖出：利润 = 2-1 = 1
 *    - 第2天买入第3天卖出：利润 = 3-2 = 1
 *    - 第3天买入第4天卖出：利润 = 4-3 = 1
 *    - 第4天买入第5天卖出：利润 = 5-4 = 1
 *    - 总利润 = 1+1+1+1 = 4（等同于直接第1天买入第5天卖出）
 * 5. 实际上就是把所有相邻的正差值累加起来
 * 
 * 时间复杂度：O(n)
 * 空间复杂度：O(1)
 */
function maxProfit(prices: number[]): number {
    // resProfit：累积的总利润
    let resProfit: number = 0;
    
    // 从第二天开始遍历
    for (let i = 1, length = prices.length; i < length; i++) {
      // 贪心策略：只要今天价格比昨天高，就进行交易
      // 相当于昨天买入，今天卖出，获得差价利润
      // Math.max(prices[i] - prices[i - 1], 0) 确保只累加正利润
      resProfit += Math.max(prices[i] - prices[i - 1], 0);
    }
    
    return resProfit;
  }
  
export { maxProfit };